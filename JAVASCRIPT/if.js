/* Problem Statement
Variables: balance = 500, price = 700

Check if (balance >= price) → Print "Can buy!" or "Insufficient funds"

let temp = 35 → If temp > 30 print "Hot weather!"*/

let balance = 500;
let price = 700;

if (balance >= price) {
    console.log("Can Buy!");
} else {
    console.log("Insufficient Funds");
}

let temp = 35;

if (temp > 30) {
    console.log("Hot Weather!");
}